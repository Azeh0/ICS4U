public class DataClassPeople  

  { 

   String Name, LastName, Team, Position; 

   int Rank, GamesPlayed, Goals, Assist; 

   

      
     public DataClassPeople(String Name, String LastName, String Team, String Position, int Rank, int GamesPlayed, int Goals, int Assist)       

     { 

       
       this.Name = Name; 
      
       this.LastName = LastName;

       this.Team = Team; 

       this.Position = Position; 
        
       this.Rank = Rank; 

       this.GamesPlayed = GamesPlayed; 

       this.Goals = Goals; 
        
       this.Assist = Assist; 


     } 

  } 
  
  
  